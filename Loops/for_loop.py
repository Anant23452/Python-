for i in range(1,4):#range(1,4) generates a sequence of numbers from 1 to 3 (inclusive)
    print(i) #prints each number in the sequence


for i in range(1,11):
        print(5*i)
         #prints the first 10 multiples of 5 (5, 10, 15, ..., 50)


# The range function is used to generate a sequence of numbers, which can be iterated over in a for loop.
# The first argument is the starting number, the second argument is the ending number (exclusive), and the third argument is the step size (optional). If only one argument is provided, it is treated as the ending number, and the starting number defaults to 0 with a step size of 1.